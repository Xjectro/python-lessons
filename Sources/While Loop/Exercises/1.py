counter = 1  # We gave 1 to our starting number and the counter will start from 1.
target_number = 5  # The target number will continue until this number is completed.
while (
    counter <= target_number
):  # We wrote 5 to the target number definition and our loop continues until it reaches 5.
    print(f"Counter: {counter}")
    counter += 1  # We are constantly adding numbers to the definition

print("Finish")
